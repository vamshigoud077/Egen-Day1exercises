package feetToCentimeter;

public class FeetToCentimeter {
	
	
	public double unitsConvertor(double d) {
		System.out.println("double is called here:");
		return d * 30.48;
		
	}

	public double  unitsConvertor(int i) {
		System.out.println("int value is called here:");
		return  (i * 30.48);
		
	}
}
